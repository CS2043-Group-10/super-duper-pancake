    void LoadUserHome()
    {
        CardLayout card = (CardLayout)MainPanel.getLayout();
        card.show(MainPanel, "userHome");
        ArrayList<Exam> exm = user.viewExams(connection);
        Date d = new Date();
        listModel15.clear();
        double total = 0;
        double grade = 0;
        ArrayList<Grade> g = user.viewGrades(connection);
        for (Grade e: g)
        {
            grade += e.getPoints();
            total += e.getTotal();
        }
        double ttl = grade / total;
        ttl = ttl*100;
        TotalExamsGradeLabel.setText((int)ttl + "%");
        for (Exam e: exm)
        {
            LocalDate warn;
            warn = e.getEndDate().toLocalDate().minusDays(5);
            if (d.after(Date.from(warn.atStartOfDay(ZoneId.systemDefault()).toInstant())))
            {
                listModel15.addElement(e.getName() + ": " + e.getEndDate());
            }
        }
    }
    

    private void GenerateQuestionPages(Exam exam)
    {
        //to be activated when the user clicks the start exam button
        //used to ceate the required amount of pages 
        //required and fill them with the provided questions
        //var questions is the array of all the questions in ascending order
        //var questionNames is te array of all the question names in ascending order
        
        QuestionTabs.removeAll();
        userSelectedExam = exam;
        questions = user.viewQuestions(connection, userSelectedExam);
        int i = 0;
        

        //loop through the process of creating a page for as many questions a provided
        for (Question e: questions)
        {
            //create the background pane
            JLayeredPane j = new JLayeredPane();
            j.setSize(1000, 2000);
            j.setLocation(0,0);
            //add a new tab to the tabbed pane for questions
            QuestionTabs.addTab("Question " + (i+1), j);

            //create all the UI elements
            JLabel name = new JLabel();
            name.setText("Question " + (i+1) + ": ");
            TextArea question = new TextArea();
            question.setText(e.getQuestion());
            //JTextArea answerBox = new JTextArea();
            userAnswers[i] = new JTextArea();
            

            j.add(name);
            j.add(question);
            j.add(userAnswers[i]);
        
            name.setSize(400, 20);
            name.setLocation(10, 6);
            Font d = new Font("Segoe UI", 0, 14);
            name.setFont(d);
            question.setSize(945, 130);
            question.setLocation(10, 36);
            userAnswers[i].setSize(945, 160);
            userAnswers[i].setLocation(10, 176);

            question.setEditable(false);
            i++;
            
        }
    }
